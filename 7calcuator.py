# CALCULATOR

# + is addition           [EXAMPLE: 5+5=10]
# - is subtraction        [EXAMPLE: 5-5=0]
# * is multiplication     [EXAMPLE: 5*5=25]
# / is division           [EXAMPLE: 5/5=1.0]
# // is floor division    [EXAMPLE: 5//2=2]  MEANING 5/2=2.5=2.0 or dividing and rounding off to the nearest integer
# % is modulus            [EXAMPLE: 5%2=1]   MEANING 5%2=5-(2*2)=1
# ** is exponentiation    [EXAMPLE: 5**2=25] MEANING 5 x 5 or 5^2

N1 = 10           # OR use this for inputting any number int(input("enter the first number->"))
N2 = 5            # int(input("enter the second number->"))
print("\nthe addition of N1 and N2 is---->", N1+N2)
print("the subtraction of N1 and N2 is---->", N1-N2)
print("the multiplication of N1 and N2 is---->", N1*N2)
print("the division of N1 by N2 is---->", N1/N2)
print("the floor division of N1 by N2 is---->", N1//N2)
print("the modulus of N1 and N2 is---->", N1 % N2)
print("the exponent of N1 and N2 is---->", N1**N2)
